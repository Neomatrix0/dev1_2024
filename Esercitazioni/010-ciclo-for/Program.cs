// esempio di ciclo for


for (int numero = 10; numero > 0; numero--)
{
    Console.WriteLine(numero);
}

// stesso esempio ma stampa i numeri al contrario

Console.WriteLine("Esempio contrario");

int n = 10;

for (int i = 1; i <= n; i++){
    Console.WriteLine(i);
}

//oppure

Console.WriteLine("Esempio alternativo");

for(int i = 1; i <= n;){
    
    Console.WriteLine(i);
    i++;
}